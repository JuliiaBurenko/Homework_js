function findValueByKey(companyName){
    if (company.name===companyName) {
        return company
    }
    if (!company.clients) {
        return null;
    }
    let subCompArr = company.clients
    while (subCompArr.length>0) {
        const nextCompany=subCompArr.pop();
        if (nextCompany.name===companyName) {
            return nextCompany;
        }

        if (nextCompany.partners && nextCompany.partners.length>0) {
            subCompArr = subCompArr.concat(nextCompany.partners);
        }
    }
    return null;
}

